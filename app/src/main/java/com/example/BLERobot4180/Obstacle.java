package com.example.BLERobot4180;

public class Obstacle {

    private float startX;
    private float startY;


    public Obstacle(float startX, float startY) {
        this.startX = startX;
        this.startY = startY;
    }

    public float getStartX() {
        return startX;
    }

    public float getStartY() {
        return startY;
    }
}
